#CICLO WHILE

contador = 1
while contador <= 5:
    print(contador)
    contador += 1 #incrementa la variable en 1
    
    
#CICLO FOR
cadena = "hola mundo"
for letra in cadena:
    print(letra, end=" ")
     
    
frutas = ["platano","fresa","melon"]
for fruta in frutas:
    print(fruta)
    
    
numero = 1
suma = 0
while numero < 6:
    suma += numero
    numero += 1
print(suma)